  [Command("unitydocs")]
  [Description("Get links to official Unity scripts documentation.")]
  public async Task UnityDocsCommand(CommandContext ctx, [RemainingText] string what) {
    if (!SetupModule.Permitted(ctx.Guild.Id, Config.ParamType.UnityDocs, ctx.Member.Roles)) return;
    await GenerateDocLink(ctx, what);
  }

  private Task GenerateDocLink(CommandContext ctx, string what) {
    if (string.IsNullOrWhiteSpace(what)) return Task.FromResult(0);
    Utils.LogUserCommand(ctx);
    try {
      what = what.Trim().ToLowerInvariant();
      if (UnityDoclinks.ContainsKey(what)) {
        return ctx.RespondAsync("Unity documentation for " + what + ": https://docs.unity3d.com/ScriptReference/" + UnityDoclinks[what]);
      }
      // Try to find something similar
      int mindist0 = int.MaxValue;
      int mindist1 = int.MaxValue;
      int mindist2 = int.MaxValue;
      string best0 = null;
      string best1 = null;
      string best2 = null;
      foreach (string key in UnityDoclinks.Keys) {
        int dist = Distance(what, key);
        if (dist < mindist0) {
          mindist2 = mindist1;
          mindist1 = mindist0;
          mindist0 = dist;
          best2 = best1;
          best1 = best0;
          best0 = key;
        }
        else if (dist < mindist1) {
          mindist2 = mindist1;
          mindist1 = dist;
          best2 = best1;
          best1 = key;
        }
        else if (dist < mindist2) {
          mindist2 = dist;
          best2 = key;
        }
      }
      if (mindist0 > 10) return Utils.DeleteDelayed(30, ctx.RespondAsync("I cannot find anything related to `" + what + "` in Unity documentation").Result);
      if (mindist2 < 600) {
        return ctx.RespondAsync("Best things I can find in Unity documentation for _**" + what + "**_ are `" + best0 + "`, `" + best1 + "`, and `" + best2 + "`:\n" +
          " https://docs.unity3d.com/ScriptReference/" + UnityDoclinks[best0] + "\n" +
          " https://docs.unity3d.com/ScriptReference/" + UnityDoclinks[best1] + "\n" +
          " https://docs.unity3d.com/ScriptReference/" + UnityDoclinks[best2]);
      }
      else if (mindist1 < 6000) {
        return ctx.RespondAsync("Best things I can find in Unity documentation for _**" + what + "**_ are `" + best0 + "` and `" + best1 + "`:\n" +
          " https://docs.unity3d.com/ScriptReference/" + UnityDoclinks[best0] + "\n" +
          " https://docs.unity3d.com/ScriptReference/" + UnityDoclinks[best1]);
      }
      else {
        return ctx.RespondAsync("Best thing I can find in Unity documentation for _**" + what + "**_ is `" + best0 + "`: " +
          " https://docs.unity3d.com/ScriptReference/" + UnityDoclinks[best0]);
      }

    } catch (Exception ex) {
      return ctx.RespondAsync(Utils.GenerateErrorAnswer("WhoIs", ex));
    }
  }

  int Distance(string a, string b) {
    if (a == b) return 0;
    float len = Math.Min(a.Length, b.Length);
    double jw = StringDistance.JWDistance(a, b);
    float dl = StringDistance.DLDistance(a, b) / len;
    float xtra = (10 + Math.Abs(a.Length - b.Length)) / len;
    float cont = (a.IndexOf(b) != -1 || b.IndexOf(a) != -1) ? .1f : 1;
    return (int)(1000 * jw * dl * xtra * cont);
  }



